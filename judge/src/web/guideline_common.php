        $between_text = preg_replace($tag_pattern, '', $between_text);
        $lines = explode("\n", $between_text);
        foreach ($lines as $line) {
            $trimmed = trim($line);
            if ($trimmed !== '' && !str_starts_with($trimmed, '#include')) {
                $blocks[] = [
                    'type' => 'text',
                    'content' => $trimmed
                ];
            }
        }

        // ë‹¤ìŒ ê²€ìƒ‰ ìœ„ì¹˜ ê°±ì‹ 
        $pos = $next_pos;
    }

    return $blocks;
}
