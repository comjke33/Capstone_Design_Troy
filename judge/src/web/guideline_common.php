        }

        // ì¶”ì¶œëœ íƒœê·¸ì™€ ê·¸ ì•ˆì˜ ë‚´ìš© ì²˜ë¦¬
        $tag_type = $matches[1][0];  // ì˜ˆ: func_def
        $tag_index = $matches[3][0]; // ì˜ˆ: 1
        $content = $matches[4][0];   // íƒœê·¸ ì‚¬ì´ì˜ ì‹¤ì œ ë‚´ìš©

